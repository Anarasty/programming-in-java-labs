public class FourZeroFourException extends Exception {
    public FourZeroFourException() {
    }

    public FourZeroFourException(String message) {
        super(message);
    }

    public FourZeroFourException(String message, Throwable cause) {
        super(message, cause);
    }

    public FourZeroFourException(Throwable cause) {
        super(cause);
    }

    public FourZeroFourException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}

